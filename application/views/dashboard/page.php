        Japan is going through a period of exceptionally poor weather. A teenage girl named Hina Amano sees a pool of sunlight at a ruined shrine on the top of a dilapidated building while attending to her dying mother. She leaves her mother's bedside and goes to the shrine, crosses its gate, and is momentarily transported to a world in the sky.

High school freshman Hodaka Morishima runs away from his home on an isolated island and moves to Tokyo. On the trip, he almost drowns due to a freak rainstorm that hits the ferry, but he is saved by a man named Keisuke Suga, who gives Hodaka his business card. In Tokyo, Hodaka struggles to support himself because he is too young to work legally. He briefly encounters Hina when he eats at the fast-food restaurant she works at, and she takes pity on him by giving him a free meal because he has eaten there for three nights in a row. However, he mostly lives his days in isolation before finally finding a job as an assistant for a small publishing company specializing in articles about urban legends with Suga, the man from the ferry. There, he meets Natsumi, a woman free-loading at Suga's place and Suga's supposed 'mistress'. Together, the two investigate urban legends relating to the crazy change in weather in Tokyo. The two learn the legend of 'sunshine girl' and 'rain girl', a girl who can control the weather from a psychic.
<br><br>
On a rainy day, Hodaka witnesses Hina being dragged by a shady club host and rushes in to save her. The two escape to a nearby abandoned building where an enraged Hina yells at Hodaka for interfering. Eventually, Hina won over by Hodaka's bravery and concern, calms down and shows him her power to clear the skies by praying. Inspired by the idea of 'sunshine girl', he proposes to Hina to start a business where she provides her mysterious ability to people who want clear skies for special events. Meanwhile, Suga and Natsumi, investigate urban legends surrounding the mysterious girl who can make the rainy weather turn to sunshine. They visit a priest who tells them the legend of a weather maiden whose power comes at a great price.
<br><br>
At Hina's place, Hodaka hides in the bathroom as the police question Hina if she encountered Hodaka before. It's revealed that Hodaka's parents filed a missing person report and they are to bring him back. An officer informs Hina that social services will be called in to take away Nagi, Hina's little brother, as the two are minors with no legal guardian. Once they leave, Hodaka emerges from his hiding place and visits Suga in his car, who fires him from his job and gives him severance pay. Hodaka then makes up his mind to run away with both Hina and Nagi.
<br><br>
The two end up at a hotel for the night where Hina reveals that her body is slowly being turned into water. She explains that she's the cause for the abnormal weather and the only way to return it to normal is for her to disappear. Hodaka refuses to let this happen and promises to protect Hina. The next day, Hodaka finds Hina missing and sets out to find her. Hodaka is chased down the police until he reaches the building with the shrine, the place where Hina first got her powers. Inside, he encounters Suga, who attempts to stop him and turn him in. The police enter and Hodaka tries to run away but is tackled by an officer. Suga saves Hodaka by holding down the officer down, giving him an opportunity to escape. Hodaka jumps into the shrine where he's transported above the sky and rescues Hina. The two hold hands as they fall through the sky with Hodaka begging Hina to start living for herself.
<br><br>
Three years later, Hodaka graduates from high school and returns to a half-submerged Tokyo to reunite with Hina, whom he hasn't spoken to in years. On his way to her house, he sees Hina praying and the two are reunited at last.
